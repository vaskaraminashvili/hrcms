                    $record['poz'],
                    $record['act']
                );
            }
        }
    }

    /**
     * Insert a record into the new table
     */
    private function insertRecord($name, $parentId, $order, $isActive): int
    {
        // Skip if name is empty
        if (empty($name)) {
            return 0;
        }

        $slug = Str::slug($name);

        // Ensure unique slug by adding a random suffix if needed
        $originalSlug = $slug;
        $counter = 1;
        while (DB::table('departments')->where('slug', $slug)->exists()) {
            $slug = $originalSlug.'-'.$counter;
            $counter++;
        }

        $id = DB::table('departments')->insertGetId([
            'name' => $name,
            'slug' => $slug,
            'description' => null,
            'color' => null,
            'author_id' => 1,
            'status' => DepartmentStatus::ACTIVE->value,
            'vacancy_count' => 3,
            'parent_id' => $parentId,
            'order' => $order,
            'created_at' => now(),
            'updated_at' => now(),
        ]);

        return $id;
    }
